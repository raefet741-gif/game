// public/js/i18n.js
// Client-side translations for English / French / Arabic. The server sends prompt
// text as {en,fr,ar} and log events as {key,params}; everything else (UI chrome,
// powers, categories, validation toasts) is translated here.

export const LANGS = [
  { code: "en", name: "English", dir: "ltr" },
  { code: "fr", name: "Français", dir: "ltr" },
  { code: "ar", name: "العربية", dir: "rtl" },
];

let lang = detectLang();

function detectLang() {
  const stored = localStorage.getItem("spill.lang");
  if (stored && LANGS.some((l) => l.code === stored)) return stored;
  const nav = (navigator.language || "en").slice(0, 2);
  if (nav === "fr") return "fr";
  if (nav === "ar") return "ar";
  return "en";
}

export function getLang() {
  return lang;
}
export function setLang(code) {
  if (!LANGS.some((l) => l.code === code)) return;
  lang = code;
  localStorage.setItem("spill.lang", code);
  applyDir();
}
export function isRTL() {
  return (LANGS.find((l) => l.code === lang)?.dir || "ltr") === "rtl";
}
export function applyDir() {
  const meta = LANGS.find((l) => l.code === lang) || LANGS[0];
  document.documentElement.lang = lang;
  document.documentElement.dir = meta.dir;
}

function interp(str, params = {}) {
  return String(str).replace(/\{(\w+)\}/g, (_, k) => (params[k] != null ? params[k] : ""));
}

