/* ---------------- public API ---------------- */
export function t(key, params) {
  const table = UI[lang] || UI.en;
  const s = table[key] ?? UI.en[key] ?? key;
  return interp(s, params);
}
export function tType(v) {
  return (WORDS[lang] || WORDS.en).type[v] || v;
}
export function tLetter(v) {
  return (WORDS[lang] || WORDS.en).letter[v] || v;
}
export function tIntensity(v) {
  return (WORDS[lang] || WORDS.en).intensity[v] || v;
}
export function tPower(id) {
  return (POWER_I18N[lang] && POWER_I18N[lang][id]) || POWER_I18N.en[id] || { name: id, blurb: "", note: "" };
}
export function tCategory(slug) {
  return (CAT[lang] && CAT[lang][slug]) || CAT.en[slug] || slug;
}
export function tErr(msg) {
  if (lang === "en") return msg;
  return SERVER_ERRORS[msg]?.[lang] || msg;
}
export function tLog(entry) {
  if (!entry) return "";
  const { key, params = {} } = entry;
  const p = { ...params };
  if (p.type) p.type = tType(p.type);
  if (p.other) p.other = tType(p.other);
  if (p.powerId) p.power = tPower(p.powerId).name;
  p.dd = params.doubled ? (WORDS[lang] || WORDS.en).ddTag + " " : "";
  const tmpl = (LOG[lang] && LOG[lang][key]) || LOG.en[key] || `[${key}]`;
  return interp(tmpl, p);
}
